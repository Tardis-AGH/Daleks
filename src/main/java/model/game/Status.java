package model.game;

/**
 * The enum Status.
 */
public enum Status {

    /**
     * Skip move status.
     */
    SKIP_MOVE,
    /**
     * Continue game status.
     */
    CONTINUE_GAME,
    /**
     * Level up status.
     */
    LEVEL_UP,
    /**
     * Restart game status.
     */
    RESTART_LEVEL,
    /**
     * Game over status.
     */
    GAME_OVER
}
