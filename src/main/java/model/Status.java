package model;

/**
 * The enum Status.
 */
public enum Status {
    /**
     * Continue game status.
     */
    CONTINUE_GAME,
    /**
     * Level up status.
     */
    LEVEL_UP,
    /**
     * Restart game status.
     */
    RESTART_GAME,
    /**
     * Game over status.
     */
    GAME_OVER
}
